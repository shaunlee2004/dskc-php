<p>
	These are rules and regulations for the Dominion Shotokan Karate Club,
	LLC (DSKC). They have been designed to help students feel more
	comfortable from the beginning and instill a basic knowledge of the
	culture, terminology and procedures that are required in a <span
		class="japaneseTerm">dojo</span>. It will require some time and effort
	during your training to learn the proper procedures, rules of etiquette
	and aspects of Japanese culture. This is understood as practicing
	karate is much different than most endeavors you have undertaken in
	your life thus far. You will make mistakes; learn from them. If you
	have questions, feel free to ask the <span class="japaneseTerm">sensei</span>or
	a <span class="japaneseTerm">senpai</span> at the proper time. We are
	all here to help you in your journey.
</p>
<p>
	We practice the Japanese art of Shotokan Karate and are affiliated with
	the International Shotokan Karate Federation (ISKF) headquartered in
	Philadelphia, Pennsylvania. The chairman and chief instructor is Master
	Teruyuki Okazaki, 10th Dan and a direct student of the founder of
	Shotokan, Master Gichin Funakoshi. All students are required to become
	members of the ISKF and can participate in all ISKF tournaments,
	special trainings, Master Camps and Fall Camps. In addition, students
	who are traveling should be able to practice at any ISKF <span
		class="japaneseTerm"> dojo</span>, worldwide, for a nominal fee. You
	will need to see your instructor prior to your trip to obtain a letter
	of introduction for the <span class="japaneseTerm">sensei</span> at the
	other <span class="japaneseTerm">dojo</span>, to discuss the small
	gift, <span class="japaneseTerm">omiyage</span>, you should bring to
	the <span class="japaneseTerm">sensei</span> and about your expected
	conduct. Remember, you are representing our <span class="japaneseTerm">dojo</span>;
	follow all their rules and do your best during your training visit.
</p>
