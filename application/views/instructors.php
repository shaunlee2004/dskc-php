<p>Steven W. Majors was born and raised in Trenton, NJ. He began his
	Shotokan training in 1985 with John Harders. In addition to Shotokan,
	Mr. Majors holds a brown belt in Judo and has also trained in Mo Duk
	Kwon, Tae Kwon Do, and Tai Chi Chuan. In August, 1994, he began his
	studies in the International Shotokan Karate Federation (ISKF). Mr.
	Majors has completed the ISKF Instructor Training Program and is a
	certified instructor, examiner and judge. He continues to train
	periodically with Master Okazaki in Philadelphia. Currently, he is
	teaching Shotokan at two Fairfax County community recreation centers
	under a cooperative program with the Fairfax County Recreation
	Department. Mr. Majors holds a Master of Arts degree from C. W. Post
	Center of Long Island University, a Bachelor of Fine Arts from Virginia
	Commonwealth University, and an Associate of Arts from Mercer County
	Community College in Trenton, NJ. He was director of the Torpedo
	Factory Art Center in Alexandria, VA for eight years and Curator of the
	Washington County Museum of Fine Arts in Hagerstown, MD for nearly four
	years. An artist as well, his work has been exhibited in New York, New
	Jersey, Pennsylvania, Maryland, Virginia, and West Virginia.</p>


