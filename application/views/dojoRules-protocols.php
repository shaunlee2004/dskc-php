<p>
	When the class begins, students will line up facing <span
		class="japaneseTerm">shomen</span> (the front of the <span
		class="japaneseTerm">dojo</span>), in <span class="japaneseTerm">musubi
		dachi</span>, according to rank, with the higher ranking member to
	your left. <span class="japaneseTerm">Sensei</span> will take a
	position in front of the class and sit in <span class="japaneseTerm">zazen</span>
	(formal seated position). The ranking student will announce:
</p>
<br/><!-- hack to line up opening ceremony OL -->
<ol>
	<li><span class="japaneseTerm">Seiza!</span> This is the command to sit
		in the formal seated position. Students will kneel, in order of rank,
		until everyone is seated. You will be instructed about these
		procedures on your first day by a senior student.</li>
	<li><span class="japaneseTerm">Mokuso!</span> This is a brief period of
		meditation, self reflection or personal preparation. You close your
		eyes, breathe in slowly through your nose and then exhale slowly
		through your mouth. Remain still and quiet.</li>
	<li><span class="japaneseTerm">Mokuso Yame!</span> Open your eyes and
		remain quietly seated.</li>
	<li><span class="japaneseTerm">Shomen ni Rei!</span> Bow to the front
		of the <span class="japaneseTerm">dojo</span>. Please note: This has
		no religious significance. It is only showing respect to Masters
		Funakoshi, Nakayama, and Okazaki.</li>
	<li><span class="japaneseTerm">Sensei ni Rei!</span> Bow to <span
		class="japaneseTerm">sensei</span>. You should say, <span
		class="japaneseTerm">osu!</span>, and maintain your bow until <span
		class="japaneseTerm">sensei</span> finishes bowing. When you rise, you
		must wait for the person to your left to stand before you stand. Once
		everyone is standing, you may move to your position on the floor.</li>
</ol>
<p>
	Once everyone is in position, a <span class="japaneseTerm">sempai</span>
	will lead the class in warm up. Proper respect and attitude will be
	expected at all times. You are in class to train. Interruptions and
	misbehaving will not be tolerated! It is not only rude and
	disrespectful, it is also dangerous. Safety is our first concern. If
	you need to leave the floor for any reason, you must have <span
		class="japaneseTerm">sensei&#8217;s</span> permission. There are no
	breaks for drinks or to use the restroom; do these things before class
	starts. If it is excessively hot or the training has been very
	difficult, <span class="japaneseTerm">sensei</span> may allow a break.
	If so, be quick and return promptly.
</p>
<p>
	Your karate training will consist mainly of three parts, <span
		class="japaneseTerm">kihon, kata</span> and <span class="japaneseTerm">kumite</span>.
	Never ask <span class="japaneseTerm">sensei</span>, &#8220;What are we
	going to do today?&#8221; It is considered rude and impolite to ask.
	You are there to do whatever you are asked to do. Ideally, we will work
	on all aspects of karate in each class; however, this may not always be
	the case. Be prepared for anything. Have your gloves and mouth guard
	with you for each class. Training includes:
</p>
<ol>
	<li><span class="japaneseTerm">Kihon</span> - These are the basics of
		karate. It includes all blocks, strikes, punches, kicks and stances.</li>
	<li><span class="japaneseTerm">Kata</span> - The <span
		class="japaneseTerm">kata</span>, or forms, are a series of blocks,
		strikes, kicks, punches and stances combined to simulate an attack by
		several opponents. Every <span class="japaneseTerm">kata</span> begins
		with a block to foster the idea of karate as a method of self-defense,
		not attack.</li>
	<li><span class="japaneseTerm">Kumite</span> - This is sparring or
		fighting. <span class="japaneseTerm">Kumite</span> is geared
		specifically to each level of training and gets progressively more
		intense as the student increases in rank.</li>
</ol>
<p>
	At the end of class, we often have cool-down stretches, partner
	stretches or, in the case of the children&#8217;s classes,
	&#8220;animal exercises.&#8221; These last exercises are done when we
	have extra time and are designed to build muscle and stamina in the
	children, most of whom are too young to be lifting weights. They
	consist of twelve sets of movements, most named after the animal they
	resemble. The group includes: monkey, bear, seal, cricket, duck walk,
	duck hop, frog, spider, bunny, crab, elephant, and military crawl. We
	also do suicide sprints, jogging, and other cardiovascular exercises in
	the <span class="japaneseTerm">dojo</span>.
</p>

